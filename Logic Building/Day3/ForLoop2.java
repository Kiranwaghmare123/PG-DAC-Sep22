
class ForLoop2{

public static void main(String args[]){
		
	for(int i=0,j=9;j>0;i++,j--)
	{	
		System.out.println("i= " +i);
		System.out.println("j=" +j);
		System.out.println(i+"---"+j);
		System.out.println(i+j);
		System.out.println(i+" "+j);
		System.out.println("---------------");
		
	}
	
	
	
} 
}
 
