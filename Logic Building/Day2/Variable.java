
class Variable{

public static void main(String args[]){
	int i = 11;
	int j = 12;
	//Addition
	int k = i+j;
	System.out.println(k);
	//Concatination
	System.out.println("Sum= "+k);
	System.out.println(i+j);
	System.out.println("Sum= "+(i+j));
	System.out.println("Sum of "+i+" and "+j+" is "+k);
	
}
}
